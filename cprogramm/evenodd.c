// program to identify even and odd
# include <stdio.h>
int main()
{
	int n;
	
	printf("please enter the number ");
	scanf("%d",&n);
	if(n%2 ==0)
	{
		printf("the number entered is even");
	}
	else
	{
		printf("the number entered is odd");
		
	}
	
	return 0;
	
}






















